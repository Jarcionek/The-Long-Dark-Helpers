package uk.co.jpawlak.thelongdark.mapnotes;

public class Marker {

    private final double x;
    private final double y;

    public Marker(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}
